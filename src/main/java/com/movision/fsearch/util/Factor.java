package com.movision.fsearch.util;

/**
 * 因数
 * @author jianglz
 * @since 2016/8/2.
 */
public class Factor {

    //权重值
    private double weight;

    //评分值
    private double score;

    public double getWeight() {
        return weight;
    }

    public void setWeight(double weight) {
        this.weight = weight;
    }

    public double getScore() {
        return score;
    }

    public void setScore(double score) {
        this.score = score;
    }
}
